public class OperadoresRelacionais {
    public static void main(String[] args) throws Exception {
        // Operadores Relacionais

        int num1 = 5;
        int num2 = 10;

        System.out.println(5 != 10);   // Saída: true
        System.out.println(5 > 10);   // Saída: false
        System.out.println(5 == 10);   // Saída: false
        System.out.println(5 >= 10);   // Saída: false
        System.out.println(5 < 10);    // Saída: true
        System.out.println(5 <= 10);   // Saída: true
        
        

    }
}